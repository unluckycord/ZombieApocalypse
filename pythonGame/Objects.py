class Objects:
    def __init__(self, objectsCount, posx, posy, sprite):
        self.objectsCount = objectsCount
        self.posx = posx
        self.posy = posy
        self.sprite = sprite
        
    def getObjectsCount(self):
        return self.getObjectsCount
    def getPosX(self):
        return self.posx
    def getPosY(self):
        return self.posy
    def getSprite(self):
        return self.sprite